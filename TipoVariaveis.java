package operadores;

public class TipoVariaveis {
	public static void main(String args[]) {
		int a = 2;
		long b = 2;
		char c = 'a';
		String d = "oi";
		boolean e=true;
		float f = 2.1F;
		double g = 2.0;
		
		System.out.printf("Valor inteiro %d\n", a);
		System.out.printf("Valor inteiro long %d\n", b);
		System.out.printf("Valor inteiro %c\n", c);
		System.out.printf("Valor inteiro %s\n", d);
		System.out.printf("Valor inteiro %b\n", e);
		System.out.printf("Valor inteiro %.10f\n", f);
		System.out.printf("Valor inteiro %.2f\n", g);
	}
	}
